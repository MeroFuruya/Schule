# Eine Schleife ist eine Möglichkeit, einen Codeblock mehrmals auszuführen.
# Die Schleife wird durch die Schlüsselwörter "for" und "while" eingeleitet.
# Der Codeblock der Schleife kann sich während der ausführung verändernde Parameter enthalten.

# Die While-Schleife wird solange ausgeführt, bis ihre Bedingung nicht mehr erfüllt ist.
# Die For-Schleife wird solange ausgeführt, bis sie alle Elemente eines Iterierbaren Objekts durchlaufen hat.

# Schleifenkopf:
# Der Schleifenkopf bildet den Anfang der Schleife.
# Der Schleifenkopf einer While-Schleife enthält die Bedingung, die erfüllt sein muss, damit die Schleife ausgeführt wird.
# Der Schleifenkopf einer For-Schleife enthält die Variable, die die Elemente des Iterierbaren Objekts enthält.

# Schleifenrumpf:
# Der Schleifenrumpf bildet den Codeblock, der bei jedem Durchlauf der Schleife ausgeführt wird.


